void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char c;

	if (n < 0)
	{
		c = 78;
		ft_putchar(c);
	}
	else if (n >= 0)
	{
		c = 80;
		ft_putchar(c);
	}
}
