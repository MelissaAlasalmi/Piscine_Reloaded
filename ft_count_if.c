int	ft_count_if(char **tab, int (*f)(char*))
{
	int k;
	int l;

	k = 0;
	l = 0;
	while (tab[k] != '\0')
	{
		if (f(tab[k]) == 1)
			l++;
		k++;
	}
	return (l);
}
